/*** Definitions here for the constants: */
/****** FL_MIN     Minimum value of `float` */
/****** DB_MIN     Minimum value of `double` */
/****** LD_MIN     Minimum value of `long double` */
/****** FL_MIN_U   Unnormalized minimum value of `float` */
/****** DB_MIN_U   Unnormalized minimum value of `double` */
/****** LD_MIN_U   Unnormalized minimum value of `long double` */
/****** FL_MAX     Maximum value of `float` */
/****** DB_MAX     Maximum value of `double` */
/****** LD_MAX     Maximum value of `long double` */
/****** ROUNDING   Rounding mode */
/****** E_RADIX    Exponent radix */
/****** FL_EPSILON Difference between `1.0` and the next representable value of `float` */
/****** DB_EPSILON Difference between `1.0` and the next representable value of `double` */
/****** LD_EPSILON Difference between `1.0` and the next representable value of `long double` */
/****** FL_E_MIN   Minimum value of `float` exponent */
/****** DB_E_MIN   Minimum value of `double` exponent */
/****** LD_E_MIN   Minimum value of `long double` exponent */
/****** FL_E_MAX   Maximum value of `float` exponent */
/****** DB_E_MAX   Maximum value of `double` exponent */
/****** LD_E_MAX   Maximum value of `long double` exponent */



